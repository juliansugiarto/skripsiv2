class ContestWorkspace < Workspace
  #                                                                       Constant
  # ==============================================================================


  #                                                                  Attr Accessor
  # ==============================================================================


  #                                                                       Callback
  # ==============================================================================
  before_create do
    set_default_attributes
  end

  #                                                                          Field
  # ==============================================================================
  field :step, default: "agreement" # agreement, revision, completed

  #                                                                       Relation
  # ==============================================================================
  belongs_to :contest # Flag only

  #                                                          Through Relationships
  # ==============================================================================
  # Since mongoid not provide "through relationships" like ActiveRecord, so we
  # use these method to provide "through relationships"
  # ==============================================================================


  #                                                                     Validation
  # ==============================================================================


  #                                                                   Class Method
  # ==============================================================================


  #                                                                         Method
  # ==============================================================================
  def set_default_attributes
    self.step = "agreement" if self.step.blank?
  end

end
